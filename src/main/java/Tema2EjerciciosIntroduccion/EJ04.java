package Tema2EjerciciosIntroduccion;

public class EJ04 {
    
    public static void main(String[] args) {
        
        int a = 10;
        int b = 20;
        
        boolean varBoolean1 = a<b;
        boolean varBoolean2 = a>b;
        boolean varBoolean3 = a==b;
        boolean varBoolean4 = a!=b;
        
        System.out.println(" Resultado de 10<20: " + varBoolean1);
        System.out.println(" Resultado de 10>20: " + varBoolean2);
        System.out.println(" Resultado de 10==20: " + varBoolean3);
        System.out.println(" Resultado de 10!=20: " + varBoolean4);
              
    }
    
}
